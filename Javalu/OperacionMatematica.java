class OperacionMatematica{
	public static void main (String arg[]){
		int num1,num2,resultado;
		num1=15;
		num2=23;
		//suma
		resultado=num1+num2;
		System.out.println(num1 + " + " + num2 + " = " + resultado);
		resultado=num1-num2;
		System.out.println(num1 + " - " + num2 + " = " + resultado);
		resultado=num1*num2;
		System.out.println(num1 + " * " + num2 + " = " + resultado);
		resultado=num1/num2;
		System.out.println(num1 + " / " + num2 + " = " + resultado);
	}
}