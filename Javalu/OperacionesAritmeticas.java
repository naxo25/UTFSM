class OperacionesAritmeticas{
	public static void main (String arg[]){
		int num1,num2,resultado;
		num1=15;
		num2=23;
		resultado=num1+num2;
		System.out.println(num1 + "+" + num2 + "=" + resultado);
		
		resultado=num1-num2;
		System.out.println(num1 + "-" + num2 + "=" + resultado);
		
		
		resultado=num1*num2;
		System.out.println(num1 + "*" + num2 + "=" + resultado);
		
		
		resultado=num1/num2;
		System.out.println(num1 + "/" + num2 + "=" + resultado);
		
	}
}